export const getInitials = (name) =>
  name
    .replace(/\b(Dr|Mr|Mrs|Ms|Smt)\.?\s*/gi, "")
    .split(" ")
    .filter(Boolean)
    .slice(0, 2)
    .map((part) => part[0].toUpperCase())
    .join("")
